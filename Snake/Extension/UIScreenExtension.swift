import UIKit

extension UIScreen {
    /// 屏幕宽度
    static var screenWidth: CGFloat {
        return UIScreen.main.bounds.width
    }
    /// 屏幕高度
    static var screenHeight: CGFloat {
        return UIScreen.main.bounds.height
    }
}
